import UIKit

extension UIColor {
    enum Text {
        static let black = UIColor.black
        static let white = UIColor.white
    }
    
    enum Border {
        static let gray = UIColor(named: "button_border")!
    }
    
    enum Background {
        static let white = UIColor(named: "main_background")!
    }
    
    enum Button {
        static let blackBackground = UIColor.black
    }
}
